// Event listener for country select
countrySelect.addEventListener('change', (e) => {
    const selectedCountry = e.target.value;
  
    if (selectedCountry === 'Nigeria') {
      // Populate states for Nigeria
      nigeriaStates.forEach((state) => {
        const option = document.createElement('option');
        option.value = state.id;
        option.text = state.name;
        stateSelect.appendChild(option)
         
      })
     
      // Enable state select
      stateSelect.disabled = false;
    } else {
      // Clear states and LGAs
      stateSelect.innerHTML = '<<option value="">Select State</option>>';
      lgaSelect.innerHTML = '<option value="">Select LGA</option>';
  
      // Disable state select
      stateSelect.disabled = true;
  
      // Allow user to type in state and LGA
      stateSelect.type = 'text';
      lgaSelect.type = 'text';
    }
  });
  

  
// Event listener for state select
stateSelect.addEventListener('change', (e) => {
    const selectedStateId = e.target.value;
  
    if (selectedStateId !== '') {
      // Populate LGAs for selected state
      nigeriaLgas[selectedStateId].forEach((lga) => {
        const option = document.createElement('option');
        option.value = lga.id;
        option.text = lga.name;
        lgaSelect.appendChild(option);
      });
  
      // Enable LGA select
      lgaSelect.disabled = false;
    } else {
      // Clear LGAs
      lgaSelect.innerHTML = '<option value="">Select LGA</option>';
  
      // Disable LGA select
      lgaSelect.disabled = true;
    }
  });
  
  


